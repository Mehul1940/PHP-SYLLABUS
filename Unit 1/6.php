<!-- 6. Write a program in PHP to demonstrate the use of multidimensional arrays -->

<?php

$a = [
    [1, 2],
    [2, 3],
    [3, 4]
];

echo "First value of the first array: " . $a[0][0] . "<br>";
echo "Second value of the first array: " . $a[0][1] . "<br>";

echo "First value of the second array: " . $a[1][0] . "<br>";
echo "Second value of the second array: " . $a[1][1] . "<br>";

echo "First value of the third array: " . $a[2][0] . "<br>";
echo "Second value of the third array: " . $a[2][1] . "<br>";

?>